    public static String ROM_MODE = "";

    public static String getRomMode() {
        if (TextUtils.isEmpty(ROM_MODE)) {
            if (RomUtils.isMiui()) {
                ROM_MODE = RomUtils.ROM_MIUI;
            } else if (RomUtils.isFlyme()) {
                ROM_MODE = RomUtils.ROM_FLYME;
            } else if (RomUtils.isOppo()) {
                ROM_MODE = RomUtils.ROM_OPPO;
            } else {
                ROM_MODE = "other";
            }
        }
        return ROM_MODE;
    }

    /**
     * 状态栏亮色模式，设置状态栏黑色文字、图标，
     * 适配4.4以上版本MIUIV、Flyme和6.0以上版本其他Android
     *
     * @param activity
     * @return 1:MIUUI 2:Flyme 3:android6.0
     */
    public static void statusBarLightMode(Activity activity) {
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.KITKAT) {
            String romMode = getRomMode();
            switch (romMode) {
                case RomUtils.ROM_MIUI:
                    setMIUIStatusBarDarkIcon(activity, true);
                    break;
                case RomUtils.ROM_FLYME:
//                    setMeizuStatusBarDarkIcon(activity, true);
                    StatusbarColorUtils.setStatusBarDarkIcon(activity,true);  //参数 false 白色 true 黑色
                    break;
                case RomUtils.ROM_OPPO:
                    ColorOSUtils(activity.getWindow(), true);
                    break;
                default:
                    if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.M) {
                        activity.getWindow().getDecorView().setSystemUiVisibility(View.SYSTEM_UI_FLAG_LIGHT_STATUS_BAR);
                    }
                    break;
            }
        }
    }

